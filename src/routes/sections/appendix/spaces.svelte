<h2>What is a Space?</h2>

<p>
	A <strong>space</strong> is a set equipped with structure that gives meaning to notions of
	nearness, continuity, or geometry. The specific structure determines what kind of space we have.
</p>

<h3>Examples</h3>

<p><strong>Topological Space:</strong> A set X with a collection of open sets satisfying:</p>
<ul>
	<li>The empty set and X are open</li>
	<li>Arbitrary unions of open sets are open</li>
	<li>Finite intersections of open sets are open</li>
</ul>

<p>
	<strong>Metric Space:</strong> A set X with a distance function d: X √ó X ‚Üí ‚Ñù satisfying the metric
	axioms (positivity, symmetry, triangle inequality).
</p>

<p>
	<strong>Manifold:</strong> A topological space that locally resembles Euclidean space, with smooth
	transition maps between coordinate charts.
</p>

<p>
	<strong>Algebraic Variety:</strong> Solution sets to polynomial equations, equipped with the
	Zariski topology.
</p>

