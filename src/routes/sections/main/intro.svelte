  <h2>1. Introduction</h2>
  
  <p>
    The hippocampal formation encodes spatial representations through place cells
    (O'Keefe & Dostrovsky, 1971). We propose that this encoding is functorial:
    the hippocampus implements a functor from topological spaces to their structure
    sheaves.
  </p>

  <p>
    <strong>Definition 1.1.</strong> Let <span class="math">X</span> be a topological
    space representing an environment. A <em>cognitive map</em> is a neural representation
    of the structure sheaf <span class="math">ùí™<sub>X</sub></span>.
  </p>

  <p>
    <strong>Theorem 1.2.</strong> Place cell representations satisfy the sheaf axioms:
    local observations on overlapping regions agree on intersections and glue uniquely
    to global representations.
  </p>
